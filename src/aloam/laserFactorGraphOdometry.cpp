pcl::PointCloud<PointType>::Ptr laserCloudCornerLast(new pcl::PointCloud<PointType>());
pcl::PointCloud<PointType>::Ptr laserCloudSurfLast(new pcl::PointCloud<PointType>());
pcl::PointCloud<PointType>::Ptr laserCloudFullRes(new pcl::PointCloud<PointType>());

int laserCloudCornerLastNum = 0;
int laserCloudSurfLastNum = 0;

///< transformation from current frame to world frame
gtsam::Pose3 T_w_curr(gtsam::Rot3(Eigen::Matrix3d::Identity()), gtsam::Point3(0,0,0));

///< transformation from current frame to last frame
gtsam::Pose3 T_last_curr(gtsam::Rot3(Eigen::Matrix3d::Identity()), gtsam::Point3(0,0,0));

std::queue<sensor_msgs::PointCloud2ConstPtr> cornerSharpBuf;
std::queue<sensor_msgs::PointCloud2ConstPtr> cornerLessSharpBuf;
std::queue<sensor_msgs::PointCloud2ConstPtr> surfFlatBuf;
std::queue<sensor_msgs::PointCloud2ConstPtr> surfLessFlatBuf;
std::queue<sensor_msgs::PointCloud2ConstPtr> fullPointsBuf;
std::mutex mBuf;

/// \brief undistort lidar point
void TransformToStart(PointType const *const pi, PointType *const po)
{
    //interpolation ratio
    double s;
    if (DISTORTION)
        s = (pi->intensity - int(pi->intensity)) / SCAN_PERIOD;
    else
        s = 1.0;
    
    gtsam::Pose3 T_end_start = T_last_curr.inverse();
    Eigen::Quaterniond q_end_start = T_end_start.rotation().toQuaternion().normalized();
    Eigen::Vector3d t_end_start_in_end = T_end_start.translation().vector();
    
    Eigen::Quaterniond q_point_start = Eigen::Quaterniond::Identity().slerp(s, q_end_start);
    Eigen::Vector3d t_point_start_in_point = (q_point_start * T_last_curr.rotation().toQuaternion().normalized()) * (s * t_end_start_in_end);
    
    Eigen::Vector3d point(pi->x, pi->y, pi->z);
    Eigen::Vector3d un_point = q_point_start.conjugate() * (point - t_point_start_in_point);

    po->x = un_point.x();
    po->y = un_point.y();
    po->z = un_point.z();
    po->intensity = pi->intensity;
}

/// \brief transform all lidar points to the end of the next frame
void TransformToEnd(PointType const *const pi, PointType *const po)
{
    // undistort point first   
    pcl::PointXYZI un_point_tmp;
    TransformToStart(pi, &un_point_tmp);
    Eigen::Vector3d point_in_start(un_point_tmp.x, un_point_tmp.y, un_point_tmp.z);
    
    gtsam::Pose3 T_end_start = T_last_curr.inverse();
    Eigen::Quaterniond q_end_start = T_end_start.rotation().toQuaternion().normalized();
    Eigen::Vector3d t_end_start_in_end = T_end_start.translation().vector();
    Eigen::Vector3d point_in_end = q_end_start * point_in_start + t_end_start_in_end;

    po->x = point_in_end.x();
    po->y = point_in_end.y();
    po->z = point_in_end.z();

    //Remove distortion time info
    po->intensity = int(pi->intensity);
}

